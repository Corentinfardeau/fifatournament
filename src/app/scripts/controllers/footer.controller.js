'use strict';

angular.module('fifatournament')
	.controller('FooterCtrl', function ($scope) {

		$scope.positionateFooter = function() {
			var container = document.getElementsByClassName('container')[0];
			var userScreenHeight = window.innerHeight;
			var footer = document.getElementsByClassName('footer')[0];

			console.log(userScreenHeight + ' / ' + (container.offsetHeight + footer.offsetHeight));
			if(userScreenHeight >= (container.offsetHeight + footer.offsetHeight)) {
				container.classList.add('footer-bottom');
			} else {
				container.classList.remove('footer-bottom');
			}
		}

		$scope.positionateFooter();

		window.onresize = function() {
			$scope.positionateFooter();
		}
	});
	